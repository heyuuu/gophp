func PhpConvertCyrString(str *uint8, length int, from byte, to byte) *byte {
	var from_table *uint8
	var to_table *uint8
	var tmp uint8
	var i int
	from_table = nil
	to_table = nil
	switch toupper(int(uint8(from))) {
	case 'W':
		from_table = _cyrWin1251
		break
	case 'A':

	case 'D':
		from_table = _cyrCp866
		break
	case 'I':
		from_table = _cyrIso88595
		break
	case 'M':
		from_table = _cyrMac
		break
	case 'K':
		break
	default:
		core.PhpErrorDocref(nil, 1<<1, "Unknown source charset: %c", from)
		break
	}
	switch toupper(int(uint8(to))) {
	case 'W':
		to_table = _cyrWin1251
		break
	case 'A':

	case 'D':
		to_table = _cyrCp866
		break
	case 'I':
		to_table = _cyrIso88595
		break
	case 'M':
		to_table = _cyrMac
		break
	case 'K':
		break
	default:
		core.PhpErrorDocref(nil, 1<<1, "Unknown destination charset: %c", to)
		break
	}
	if str == nil {
		return (*byte)(str)
	}
	for i = 0; i < length; i++ {
		if from_table == nil {
			tmp = str[i]
		} else {
			tmp = from_table[str[i]]
		}
		if to_table == nil {
			str[i] = tmp
		} else {
			str[i] = to_table[tmp+256]
		}
	}
	return (*byte)(str)
}

